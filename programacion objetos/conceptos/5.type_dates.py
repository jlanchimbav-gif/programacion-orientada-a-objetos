## tipos de datos en python ##

#datos primitivos 
# tipo de dato string (str)
nombre= "Jorge"
print(nombre)

#tipo de dato entero(int) 
edad= 26
print(edad)

# tipo de dato flotante(float)
altura= 1.75
print(altura)

#tipo de dato booleano(bool)
es_mayor_de_edad= True
print(es_mayor_de_edad)

# tipo de dato decimal
decimal= 10.5
print(decimal)




